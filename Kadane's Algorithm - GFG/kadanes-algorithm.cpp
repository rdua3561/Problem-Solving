class Solution{
    public:
    // arr: input array
    // n: size of array
    //Function to find the sum of contiguous subarray with maximum sum.
    long long maxSubarraySum(int nums[], int n){
        
        int res=nums[0];
        int max_end=nums[0];
        for(int i=1;i<n;i++)
        {
          max_end=max(nums[i],max_end+nums[i]);
          res=max(res,max_end);
        }
        return res;
        
    }
};

